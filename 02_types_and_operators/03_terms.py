# operandus: egy műveleten részt vevő érték(ek): 10, 20
# operátor: a műveletet végrehajtó szimbólum: +
# kifejezés: egy olyan kódrészlet, ami egy értéket ad viszsza: 10 + 20
print(10 + 20)

# utasítás: egy olyan kódrészlet, amely egy vagy több műveletet hajt végre, de nem ad vissza értéket
summa = 10 + 20
