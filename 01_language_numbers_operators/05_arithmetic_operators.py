print(10 + 10)
print(10 - 10)
print(10 * 10)
print(10 / 10)
print(10 % 3)  # modulus operator
print(10 // 3)  # floor division operator
print(10**3)  # exponentiation operator

# operator precedence: https://docs.python.org/3/reference/expressions.html
print(10 + 2 * 3)  # operator precedence
print((10 + 2) * 3)  # parentheses to change precedence
print(10 + 3 * 2**2)  # operator precedence with exponentiation
print((10 + 3) * 2**2)
